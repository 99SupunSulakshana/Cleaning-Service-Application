package com.example.cleanerservice.model;

public class Advertisement {

    private int addId;
    private String addName;
    private String addLocation;
    private String addDescription;
    private String addPackageName;
    private String addPackagePrice;
    private byte[] addFrontImage;
    private String addEmail;
    private int addContact;
    private String addDate;


    public int getAddId() {
        return addId;
    }

    public void setAddId(int addId) {
        this.addId = addId;
    }

    public String getAddName() {
        return addName;
    }

    public void setAddName(String addName) {
        this.addName = addName;
    }

    public String getAddLocation() {
        return addLocation;
    }

    public void setAddLocation(String addLocation) {
        this.addLocation = addLocation;
    }

    public String getAddDescription() {
        return addDescription;
    }

    public void setAddDescription(String addDescription) {
        this.addDescription = addDescription;
    }

    public String getAddPackageName() {
        return addPackageName;
    }

    public void setAddPackageName(String addPackageName) {
        this.addPackageName = addPackageName;
    }

    public String getAddPackagePrice() {
        return addPackagePrice;
    }

    public void setAddPackagePrice(String addPackagePrice) {
        this.addPackagePrice = addPackagePrice;
    }

    public byte[] getAddFrontImage() {
        return addFrontImage;
    }

    public void setAddFrontImage(byte[] addFrontImage) {
        this.addFrontImage = addFrontImage;
    }

    public String getAddEmail() {
        return addEmail;
    }

    public void setAddEmail(String addEmail) {
        this.addEmail = addEmail;
    }

    public int getAddContact() {
        return addContact;
    }

    public void setAddContact(int addContact) {
        this.addContact = addContact;
    }

    public String getAddDate() {
        return addDate;
    }

    public void setAddDate(String addDate) {
        this.addDate = addDate;
    }
}
